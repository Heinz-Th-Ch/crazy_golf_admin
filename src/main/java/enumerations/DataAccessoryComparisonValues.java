package enumerations;

/**
 * This enum contains all valid arguments of {@link accessories.AbstractDataAccessory}.
 */
public enum DataAccessoryComparisonValues {

    /**
     * The value for BETWEEN comparisons.
     */
    BW,
    /**
     * The value for EQUAL comparisons.
     */
    EQ,
    /**
     * The value for LIKE comparisons.
     */
    LK,
    /**
     * The value for NOT BETWEEN comparisons.
     */
    NB,
    /**
     * The value for NOT EQUAL comparisons.
     */
    NE,
    /**
     * The value for NOT LIKE comparisons.
     */
    NL

}
